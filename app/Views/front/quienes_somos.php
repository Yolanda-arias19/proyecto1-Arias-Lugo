  <h1>Quienes Somos</h1>  
  <h2>Nuestros Objetivos</h2>
  <p>En nuestra pastelería, buscamos más que solo endulzar el día:
     queremos crear momentos que se recuerden. Apostamos por una repostería artesanal, 
     con ingredientes de calidad y mucho amor en cada detalle.
    Nuestro objetivo es ofrecer productos únicos, creativos y personalizados
     que reflejen nuestra pasión y dedicación.</p>

  <h2>Nuestra Historia</h2>
  <p>Somos Virginia y Valentina, dos hermanas unidas por el amor a la repostería.
     Todo comenzó en la cocina de casa, entre risas, recetas heredadas de mama
     y mucha harina en el aire. 
     Lo que empezó como un hobby se convirtió en nuestro proyecto de vida.
     Decidimos emprender juntas para compartir con otros lo que más disfrutamos hacer:
     postres con alma y sabor casero.
     Con el tiempo fuimos creciendo, aprendiendo y mejorando.
     Hoy seguimos horneando como el primer día, 
     manteniendo la esencia de lo hecho a mano y sumando nuevas ideas
     para sorprender a cada cliente.</p>

  <h2>Nuestro Equipo</h2>
  <p>Todo comenzó entre nosotras y nuestra madre.Hoy somos nosotras dos hermanas
     que compartimos los mismos valores: pasión, creatividad y compromiso. 
     Trabajamos en conjunto,nos ayudamos a transformar cada pedido
     en una experiencia especial. 
     Y lo más importante: seguimos disfrutando cada paso del proceso, 
     desde la idea hasta el último toque de decoración.</p>
</section>
