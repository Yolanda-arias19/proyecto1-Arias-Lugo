   <h1>Comercializacion</h1>
<p>Ofrecemos varias opciones para que recibas tus productos como más te convenga:</p>

<h2>Entrega en tienda programada:</h2>
<p> Coordinamos con vos el día y horario ideal para pasar
     a buscar tu pedido por nuestro local.
</p>

<h2>Entregas para eventos:</h2>
<p> Si tenés un evento especial, llevamos todo listo al lugar indicado.</p>

<h2>Formas de Envío</h2>
<p>Realizamos entregas a domicilio con servicio propio 
   dentro de la ciudad con costo adicional según tu ubicación.
   Todos nuestros productos se transportan con el mayor cuidado para garantizar
   que lleguen frescos y en perfecto estado.</p>

<h2>Formas de Pago</h2>
<p>Aceptamos varias formas de pago para tu comodidad:</p>
<ul>
 <li>Efectivo (en tienda o contra entrega)</li>
 <li>Transferencia bancaria</li>
 <li>Mercado Pago</li>
</ul>

<h2>Información Adicional</h2>
<ul>
<li>Pedidos personalizados:
 Recomendamos hacerlos con al menos una semana de anticipación.</li>
<li>Consultas rápidas: Podés escribirnos por WhatsApp o redes sociales.</li>
<li>Política de cancelaciones: Se aceptan hasta 48 horas antes de la
   fecha de entrega.</li>
<li>Packaging: Todos nuestros productos se entregan en empaques cuidados, 
    listos para regalar o presentar.</li>
</ul>

